#!/bin/bash
BUILD_DIR=/home/hhuang/capiv2
PUSH_SCRIPT=/home/hhuang/hvim/qcom-push-so.bat
TUNING_DIR="$BUILD_DIR/capi_v2_cirrus_cspl/include/tuningheaders"
HEADERS="$BUILD_DIR/capi_v2_cirrus_cspl/source/compat.c"
DEF16_STRING="#define USE_CASE_0_TUNING \"tuningheaders"
DEF24_STRING="#define USE_CASE_0_TUNING_24BIT \"tuningheaders"
CONFIG_RX_LEFT=CSPL_CONFIG_RX_LEFT
CONFIG_RX_RIGHT=CSPL_CONFIG_RX_RIGHT

CSPL_LIB=$BUILD_DIR/capi_v2_cirrus_cspl/LLVM_Debug/capi_v2_cirrus_cspl.so

ORI_TUNING=$1


if [ ! -f "$ORI_TUNING" ]; then
	echo "tuning '$ORI_TUNING' no found!"
	exit
fi

if [ ! -n "$2" ]; then
	echo "Please set sample per bits in command line!"
	exit
fi

#go to build directory
cd $BUILD_DIR

cp -v $ORI_TUNING ./
#locate in build directory
TUNING=$(basename $ORI_TUNING)

#Json tuning or C header tuning
POSTFIX=${TUNING##*.}

#24bit tuning or 16bit tuning
if [ "$2" = "24" ]; then
	DEF_STRING=$DEF24_STRING
else
	DEF_STRING=$DEF16_STRING
fi

if [ "$POSTFIX" = "txt" ]; then
	echo "txt tuning"
	H_TUNING=$(basename ${TUNING} .txt).h
    cp -v $TUNING $H_TUNING
elif [ "$POSTFIX" = "h" ]; then
	echo "C header (*.h) tuning"
	H_TUNING=$(basename ${TUNING} .h).h
    cp -v $TUNING $H_TUNING
elif [ "$POSTFIX" = "json" ]; then
	echo "json tuning"
	# convert json to txt (C header *.h)
	H_TUNING=$(basename ${TUNING} .json).h
	json2hexagonbin.exe -t $H_TUNING $TUNING
	# C header (*.h) tuning file generated by json2hexagonbin
	# located in current directory
	if [ ! -f "$H_TUNING" ]; then
		echo "Covert json to C header (*.h) failed!"
		exit
	fi
else
	echo "Tuning format error '$POSTFIX'!"
	exit
fi

#put the tuning in C file
sed -i '/'"$DEF_STRING"'/c '"$DEF_STRING"'/'"$H_TUNING\""'' $HEADERS

#Show Checksum
#------------------------------------------------------------
RX_LEFT=$(sed -n -e '/'"$CONFIG_RX_LEFT"'/=' $H_TUNING)
RX_LEFT_STR=$(sed -n ${RX_LEFT}p $H_TUNING)
RX_LEFT_ARRAY=(${RX_LEFT_STR//\ / })
RX_LEFT_CHKSUM=${RX_LEFT_ARRAY[$(expr ${#RX_LEFT_ARRAY[@]} - 1)]}
echo $RX_LEFT_CHKSUM
#for i in "${!ARRAY[@]}"; do
#    echo "$i=>${ARRAY[i]}"
#done

# Total size : ${#ARRAY[@]}
# The last one : $(expr ${#ARRAY[@]} - 1)
RX_RIGHT=$(sed -n -e '/'"$CONFIG_RX_RIGHT"'/=' $H_TUNING)
RX_RIGHT_STR=$(sed -n ${RX_RIGHT}p $H_TUNING)
RX_RIGHT_ARRAY=(${RX_RIGHT_STR//\ / })
RX_RIGHT_CHKSUM=${RX_RIGHT_ARRAY[$(expr ${#RX_RIGHT_ARRAY[@]} - 1)]}
echo $RX_RIGHT_CHKSUM
#------------------------------------------------------------

#copy tuning to directory & clean
mv -v $H_TUNING $TUNING_DIR
rm -v $TUNING

if [ "$3" = "insert" ]; then
	echo "insert tuning: $ORI_TUNING"
	cd -
	exit
fi

#make cspl lib for hexagon
./cygwin-make-setup-sdk3.4.3.sh

# go back
cd -


if [ ! -f "$CSPL_LIB" ]; then
	echo "File '$CSPL_LIB' not found, Build Failed?"
else
	#NEW_CSPL_LIB=$(basename ${CSPL_LIB} .so)_$(date +"%Y-%m-%d_%H-%M-%S").so
	CSPL_DIR_NAME=$(date +"%Y-%m-%d_%H-%M-%S")
	#mkdir for lib
	NEW_CSPL_DIR=$(dirname ${ORI_TUNING})/$CSPL_DIR_NAME
	mkdir -p $NEW_CSPL_DIR

    #Put tuning and LIB together
	cp -v $CSPL_LIB $NEW_CSPL_DIR
	cp -v $PUSH_SCRIPT $NEW_CSPL_DIR
	#Put origin tuning to OUTPUT directory
	cp -v $ORI_TUNING $NEW_CSPL_DIR

	tar -zcvf ${NEW_CSPL_DIR}.tar.gz $NEW_CSPL_DIR
fi


#Output message
LINE_16BIT=$(sed -n -e '/'"$DEF16_STRING"'/=' $HEADERS)
LINE_24BIT=$(sed -n -e '/'"$DEF24_STRING"'/=' $HEADERS)
BRANCH=$(bash -c 'git branch --no-color 2> /dev/null' | sed -e '/^[^*]/d' -e 's/* \(.*\)/\1/' -e 's/\//\_/g')

echo "============================================="
echo "CAPI V2 CSPL lib Information:"
echo "Build Time: $(date +"%Y-%m-%d %H:%M:%S")"
echo "Branch: $BRANCH"

echo ""
#Show Tuning In Compat.c
echo "TUNING:"
echo "    16BIT: $(basename "$(sed -n ${LINE_16BIT}p $HEADERS)")"
echo "    24BIT: $(basename "$(sed -n ${LINE_24BIT}p $HEADERS)")"
echo ""
echo "CSPL LIB:"
echo "    $NEW_CSPL_LIB"
echo "CHECKSUM: $(md5sum $CSPL_LIB)"
echo "RX LEFT_CHECKSUM: $RX_LEFT_CHKSUM"
echo "RX RIGHT_CHECKSUM: $RX_RIGHT_CHKSUM"
echo ""
echo "ADB PUSH:"
echo "    Run qcom-push-so.bat in package"
echo "    "
echo "    OR"
echo "    adb root"
echo "    adb remount"
echo "    adb push capi_v2_cirrus_cspl.so /vendor/lib/rfsa/adsp/capi_v2_cirrus_sp.so"
echo "============================================="
# Open lib location
cd $(dirname $ORI_TUNING)
explorer.exe .
cd -

